import normalizeGrid from './'

describe('normalizeGrid', () =>
  it('converts a normal grid into a normalized grid', () =>
    expect(
      normalizeGrid([
        [5, 9, 8, 3, 1, 2, 6, 4, 7],
        [4, 1, 6, 5, 7, 8, 2, 9, 3],
        [2, 7, 3, 9, 6, 4, 8, 1, 5],
        [3, 8, 2, 4, 5, 1, 7, 6, 9],
        [9, 4, 7, 6, 2, 3, 5, 8, 1],
        [6, 5, 1, 8, 9, 7, 3, 2, 4],
        [7, 6, 9, 2, 4, 5, 1, 3, 8],
        [8, 2, 5, 1, 3, 9, 4, 7, 6],
        [1, 3, 4, 7, 8, 6, 9, 5, 2],
      ])
    ).toEqual({
      0: {
        0: 5,
        1: 9,
        2: 8,
        3: 3,
        4: 1,
        5: 2,
        6: 6,
        7: 4,
        8: 7,
      },
      1: {
        0: 4,
        1: 1,
        2: 6,
        3: 5,
        4: 7,
        5: 8,
        6: 2,
        7: 9,
        8: 3,
      },
      2: {
        0: 2,
        1: 7,
        2: 3,
        3: 9,
        4: 6,
        5: 4,
        6: 8,
        7: 1,
        8: 5,
      },
      3: {
        0: 3,
        1: 8,
        2: 2,
        3: 4,
        4: 5,
        5: 1,
        6: 7,
        7: 6,
        8: 9,
      },
      4: {
        0: 9,
        1: 4,
        2: 7,
        3: 6,
        4: 2,
        5: 3,
        6: 5,
        7: 8,
        8: 1,
      },
      5: {
        0: 6,
        1: 5,
        2: 1,
        3: 8,
        4: 9,
        5: 7,
        6: 3,
        7: 2,
        8: 4,
      },
      6: {
        0: 7,
        1: 6,
        2: 9,
        3: 2,
        4: 4,
        5: 5,
        6: 1,
        7: 3,
        8: 8,
      },
      7: {
        0: 8,
        1: 2,
        2: 5,
        3: 1,
        4: 3,
        5: 9,
        6: 4,
        7: 7,
        8: 6,
      },
      8: {
        0: 1,
        1: 3,
        2: 4,
        3: 7,
        4: 8,
        5: 6,
        6: 9,
        7: 5,
        8: 2,
      },
    })))
